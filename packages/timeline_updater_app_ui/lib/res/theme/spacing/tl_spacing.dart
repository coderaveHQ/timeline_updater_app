class TLSpacing {

  // The unit of spacing
  static const double _unit = 16.0;

  /// The extra extra extra small spacing value
  static const double xxxs = 0.0625 * _unit;
  /// The extra extra small spacing value
  static const double xxs =  0.125 * _unit;
  /// The extra small spacing value
  static const double xs = 0.25 * _unit;
  /// The small spacing value
  static const double sm = 0.5 * _unit;
  /// The medium spacing value
  static const double md = 0.75 * _unit;
  /// The large spacing value
  static const double lg = _unit;
  /// The extra large spacing value
